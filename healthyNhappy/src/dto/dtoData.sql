CREATE TABLE MEMBER
(
    ID VARCHAR2(50) PRIMARY KEY,
    PWD VARCHAR2(50) NOT NULL,
    NAME VARCHAR2(50) NOT NULL,
    ADDRESS VARCHAR2(50) NOT NULL,
    PHONE VARCHAR2(50) NOT NULL,
    EMAIL VARCHAR2(50)UNIQUE,
    AUTH NUMBER(1) NOT NULL
    );
    
    
CREATE TABLE PRODUCT
(
    PRD_NUM NUMBER(8) PRIMARY KEY,
    PRD_NAME VARCHAR2(50) NOT NULL,
    PRD_PRICE NUMBER(10) NOT NULL,
    PRD_IMG_NAME VARCHAR2(30) NOT NULL,
    PRD_INFO VARCHAR2(500) NOT NULL,
    PRD_DELIVERY_PRICE NUMBER(6) UNIQUE,
    PRD_SIZE VARCHAR2(50) NOT NULL,
    PRD_ORIGIN VARCHAR2(50) NOT NULL,
    PRD_EXPDATE VARCHAR2(50) NOT NULL,
    PRD_KEEP VARCHAR2(100) NOT NULL,
    PRD_PACK VARCHAR2(100) NOT NULL
    );
   
CREATE TABLE ORDER_PRD
(
    ORDER_SEQ NUMBER(8) PRIMARY KEY,
    ORDER_ID VARCHAR2(50),
    ORDER_PRD_NUM NUMBER(8),
    ORDER_AMOUNT NUMBER(8) NOT NULL,
    ORDER_REQUEST VARCHAR2(500) NOT NULL,
    ORDER_DATE DATE NOT NULL,
    ORDER_ADDRESS VARCHAR2(50) NOT NULL,
    ORDER_PRICE NUMBER(10) NOT NULL,
    ORDER_CART NUMBER(10) NOT NULL
    ); 
   
ALTER TABLE ORDER_PRD
ADD CONSTRAINT FK_ORDER_ID FOREIGN KEY(ORDER_ID)
REFERENCES MEMBER(ID);   
  
ALTER TABLE ORDER_PRD
ADD CONSTRAINT FK_ORDER_NUM FOREIGN KEY(ORDER_PRD_NUM)
REFERENCES PRODUCT(PRD_NUM); 


 CREATE TABLE PAY_INFO
(
    PAY_ORDER_SEQ NUMBER(8),
    PAY_DATE DATE NOT NULL,
    PAY_MONEY NUMBER(10) 
    ); 
    
ALTER TABLE PAY_INFO
ADD CONSTRAINT FK_PAY_SEQ FOREIGN KEY(PAY_ORDER_SEQ)
REFERENCES ORDER_PRD(ORDER_SEQ);     


